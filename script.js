let activeBatch = Object.keys(BATCHES)[0];

function initBatchTabs() {
  const bar = document.getElementById('batch-bar');
  bar.innerHTML = '';
  Object.keys(BATCHES).forEach(sy => {
    const btn = document.createElement('button');
    btn.className = 'batch-tab' + (sy === activeBatch ? ' active' : '');
    btn.innerHTML = `SY ${sy.replace('-', '–')} <span class="batch-count">${BATCHES[sy].length}</span>`;
    btn.addEventListener('click', () => {
      activeBatch = sy;
      document.querySelectorAll('.batch-tab').forEach(t => t.classList.remove('active'));
      btn.classList.add('active');
      document.getElementById('student-search').value = '';
      renderGrid(BATCHES[activeBatch]);
    });
    bar.appendChild(btn);
  });
}

function renderGrid(list) {
  const grid = document.getElementById('students-grid');
  grid.innerHTML = '';
  if (!list || list.length === 0) {
    grid.innerHTML = '<div class="batch-empty"><strong>No Students Yet</strong>List will be updated soon.</div>';
    return;
  }
  list.forEach((s, i) => {
    const card = document.createElement('div');
    card.className = 'student-card';
    card.style.animationDelay = `${Math.min(i, 8) * 0.04}s`;
    card.innerHTML = `
        <div class="avatar" style="background:${s.color};border:1px solid rgba(255,255,255,0.1)">${s.image ? `<img src="${s.image}" alt="${s.name}">` : s.name.split(' ').map(n => n[0]).join('')}</div>
        <h3>${s.name}</h3>
        <p class="role">${s.role}</p>
        <div class="badge-row">${s.badges.map(b => `<span class="badge">${b}</span>`).join('')}</div>
      `;
    card.addEventListener('click', () => openProfile(s));
    grid.appendChild(card);
  });
}

function filterStudents(val) {
  const q = val.toLowerCase();
  renderGrid(BATCHES[activeBatch].filter(s =>
    s.name.toLowerCase().includes(q) || s.role.toLowerCase().includes(q)
  ));
}

function initGrid() {
  initBatchTabs();
  renderGrid(BATCHES[activeBatch]);
}

function renderAwards() {
  const grid = document.getElementById('awards-grid');
  grid.innerHTML = AWARDS.map((a, i) => `
      <div class="award-card" style="animation-delay:${Math.min(i, 8) * 0.04}s">
        <div class="award-image">
          ${a.image ? `<img src="${a.image}" alt="${a.title}">` : `<span class="award-image-placeholder">No Image</span>`}
        </div>
        <div class="award-body">
          <p class="award-date">${a.date}</p>
          <h3>${a.title}</h3>
          <p class="award-desc">${a.desc}</p>
        </div>
      </div>
    `).join('');
}

function openProfile(s) {
  const avatar = document.getElementById('p-avatar');
  avatar.innerHTML = s.image ? `<img src="${s.image}" alt="${s.name}">` : s.name.split(' ').map(n => n[0]).join('');
  avatar.style.background = s.color;
  document.getElementById('p-name').textContent = s.name;
  document.getElementById('p-role').textContent = s.role;
  document.getElementById('p-badges').innerHTML = s.badges.map(b => `<span class="badge">${b}</span>`).join('');
  document.getElementById('p-bio').textContent = s.bio;
  document.getElementById('p-heightweight').textContent = s.height && s.weight ? `${s.height}/${s.weight}` : '-cm/-kg';
  document.getElementById('p-birthday').textContent = s.birthday || '-';
  document.getElementById('p-achievements').innerHTML = s.achievements.map((a, i) => `
      <li><div class="ach-icon">${i + 1}</div><div class="ach-text"><strong>${a.title}</strong><span>${a.desc}</span></div></li>
    `).join('');
  document.getElementById('p-interests').innerHTML = s.interests.map(i => `<span class="interest-tag">${i}</span>`).join('');
  document.getElementById('profile-overlay').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeProfilePanel() {
  document.getElementById('profile-overlay').classList.remove('open');
  document.body.style.overflow = '';
}

function closeProfile(e) {
  if (e.target === document.getElementById('profile-overlay')) closeProfilePanel();
}

function showPage(name) {
  document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
  document.querySelectorAll('.nav-links a').forEach(a => a.classList.remove('active'));
  document.getElementById('page-' + name).classList.add('active');
  document.getElementById('nav-' + name).classList.add('active');
  window.scrollTo(0, 0);
  closeNav();
}

function toggleNav() {
  document.getElementById('nav-links').classList.toggle('open');
  document.getElementById('hamburger').classList.toggle('open');
}

function closeNav() {
  document.getElementById('nav-links').classList.remove('open');
  document.getElementById('hamburger').classList.remove('open');
}

